    /**
     * Get the country name base on the code
     *
     * @param $code
     *
     * @return string|false
     */
    public static function getName($code)
    {
        $code = strtolower($code);
        return !empty(static::$countryList[$code]) ? static::$countryList[$code] : false;
    }


    /**
     * Get the country code base on the name
     *
     * @param $country
     *
     * @return string|false
     */
    public static function getCode($country) {
        $country = trim($country);
        $code = array_search(trim($country), static::$countryList);

        if(!$code) {
            // e.g. Venezuela (Bolivarian Republic of) vs Venezuela
            $search = preg_grep('~' . $country . '~', static::$countryList);
            if(count($search) === 1) {
                $country = array_shift($search);
                $code = array_search(trim($country), static::$countryList);
            }
        }
        return is_null($code) ? !!$code : $code;
    }
